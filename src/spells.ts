type UnparsedSpell = {
  slug: string;
  title: string;
  title_en: string;
  level: CardLevel | "Заговор";
  schoolSlug: SchoolSlug
  item_suffix: "В.." | "ВС." | "ВСМ" | "В.М" | ".СМ" | ".С.",
  classes: (keyof Classes)[],
  classesTce: (keyof Classes)[],
  archtype: archtype[],
  source: [102] | [107, 109] | [109] | [108, 117] | [112] | [115] | [116] | [120] | [117] | [155] | [153] | [152] | [160],
  needsConcentration: boolean,
  isRitual: boolean,
  castTimeType: CastTimeType,
}

export type Spell = UnparsedSpell & {
    school: School,
    item_icon: `spell_school_${SchoolSlug}`,
    filterText: string,
    href: string,
}

const parseSpell = (spell: UnparsedSpell): Spell => ({
    ...spell,
    school: schoolBySlug[spell.schoolSlug],
    item_icon: `spell_school_${spell.schoolSlug}`,
    filterText: `${spell.title.toLowerCase()} ${spell.title_en.toLowerCase()}`,
    href: `https://dnd.su/spells/${spell.slug}`
})

